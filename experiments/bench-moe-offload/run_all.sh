#!/bin/bash
# run_all.sh — orchestrate the bench-moe-offload experiment.
#
# Runs every cell in the execution order defined in README.md, handles the
# conditional L3b / L4b threshold rule, then generates report.md.
#
# Assumes prep.sh has already been run. Re-running is safe.

set -euo pipefail

ROOT="$(cd "$(dirname "$0")" && pwd)"
RESULTS="$ROOT/results"
BENCH="$ROOT/bench_cell.sh"
REPORT="$ROOT/report.md"

info() { echo "[run_all] $*"; }
die()  { echo "[run_all] ERROR: $*" >&2; exit 1; }

[[ -x "$BENCH" ]] || die "bench_cell.sh not executable"
[[ -f "$ROOT/prompts/hermes_4k.json" ]] || die "hermes_4k.json missing (run prep.sh)"

# peak VRAM from a completed cell (MiB)
read_vram_peak() {
    local cell="$1"
    local metrics="$RESULTS/$cell/single_metrics.json"
    [[ -f "$metrics" ]] || { echo "0"; return; }
    python3 -c "import json; print(json.load(open('$metrics'))['resources']['vram_peak_mib'])" 2>/dev/null || echo "0"
}

# 13.5 GiB = 13824 MiB
THRESHOLD_MIB=13824

# ---------- execution order ----------

info "1) L6 — expert offload + nothink (hypothesised winner)"
"$BENCH" L6

info "2) L5 — expert offload + vanilla (reasoning tax baseline)"
"$BENCH" L5

info "3) L2 — CPU-only + nothink (floor, usable)"
"$BENCH" L2

info "4) L1 — CPU-only + vanilla (absolute floor)"
"$BENCH" L1

info "5) L4a — partial N=30 + nothink"
"$BENCH" L4a
L4A_VRAM=$(read_vram_peak L4a)
info "   L4a VRAM peak: ${L4A_VRAM} MiB"

info "6) L3a — partial N=30 + vanilla"
"$BENCH" L3a
L3A_VRAM=$(read_vram_peak L3a)
info "   L3a VRAM peak: ${L3A_VRAM} MiB"

if (( L4A_VRAM > 0 && L4A_VRAM <= THRESHOLD_MIB )); then
    info "7a) L4b — partial N=35 + nothink (L4a VRAM ≤ 13.5 GiB)"
    "$BENCH" L4b
else
    info "7a) L4b SKIPPED (L4a VRAM ${L4A_VRAM} MiB > ${THRESHOLD_MIB} MiB)"
fi

if (( L3A_VRAM > 0 && L3A_VRAM <= THRESHOLD_MIB )); then
    info "7b) L3b — partial N=35 + vanilla (L3a VRAM ≤ 13.5 GiB)"
    "$BENCH" L3b
else
    info "7b) L3b SKIPPED (L3a VRAM ${L3A_VRAM} MiB > ${THRESHOLD_MIB} MiB)"
fi

# Contention: L6 and L4a already carry contention runs from bench_cell.sh
# (it detects the cell id). No extra step needed.

# ---------- generate report ----------
info "generating report.md"
python3 - "$RESULTS" "$REPORT" <<'PY'
import json, pathlib, sys

results_dir = pathlib.Path(sys.argv[1])
report_path = pathlib.Path(sys.argv[2])

cells_order = ["L1", "L2", "L3a", "L3b", "L4a", "L4b", "L5", "L6"]
rows = []
for cell in cells_order:
    metrics_file = results_dir / cell / "metrics.json"
    if not metrics_file.exists():
        metrics_file = results_dir / cell / "single_metrics.json"
    if not metrics_file.exists():
        continue
    d = json.load(open(metrics_file))
    single = d.get("single", {})
    res = d.get("resources", {})
    rows.append({
        "cell": cell,
        "flags": d.get("flags", ""),
        "prompt_per_s": single.get("median_prompt_per_second"),
        "decode_per_s": single.get("median_predicted_per_second"),
        "wall_s": single.get("median_wall_s"),
        "prompt_ms": single.get("median_prompt_ms"),
        "predicted_ms": single.get("median_predicted_ms"),
        "completion_tokens": single.get("median_completion_tokens"),
        "content_nonempty": single.get("content_nonempty_all"),
        "vram_peak_mib": res.get("vram_peak_mib"),
        "ram_peak_mib": res.get("ram_peak_mib"),
    })

def fmt(v, digits=1):
    if v is None: return "—"
    if isinstance(v, bool): return "yes" if v else "no"
    if isinstance(v, float): return f"{v:.{digits}f}"
    return str(v)

out = ["# bench-moe-offload — results\n",
       "Generated by `run_all.sh`. Raw per-run data in `results/<cell>/`.\n"]

out.append("## Single-load summary (median of 3 runs)\n")
out.append("| cell | flags | prompt tok/s | decode tok/s | wall s | prompt ms | decode ms | completion tok | content non-empty | VRAM peak MiB | RAM peak MiB |")
out.append("|---|---|---:|---:|---:|---:|---:|---:|---:|---:|---:|")
for r in rows:
    out.append("| {cell} | `{flags}` | {pps} | {dps} | {wall} | {pms} | {dms} | {ct} | {nonempty} | {vram} | {ram} |".format(
        cell=r["cell"],
        flags=r["flags"],
        pps=fmt(r["prompt_per_s"]),
        dps=fmt(r["decode_per_s"]),
        wall=fmt(r["wall_s"]),
        pms=fmt(r["prompt_ms"], 0),
        dms=fmt(r["predicted_ms"], 0),
        ct=fmt(r["completion_tokens"], 0),
        nonempty=fmt(r["content_nonempty"]),
        vram=fmt(r["vram_peak_mib"], 0),
        ram=fmt(r["ram_peak_mib"], 0),
    ))

# Contention section
contention_rows = []
for cell in ("L4a", "L6"):
    mf = results_dir / cell / "metrics.json"
    if not mf.exists(): continue
    d = json.load(open(mf))
    cont = d.get("contention")
    if not cont: continue
    contention_rows.append((cell, d.get("single", {}), cont))

if contention_rows:
    out.append("\n## Contention (Hermes ‖ Honcho deriver, median of 3)\n")
    out.append("| cell | Hermes single wall s | Hermes contended wall s | Hermes Δ s | Honcho wall s | Hermes content non-empty (contended) |")
    out.append("|---|---:|---:|---:|---:|---:|")
    for cell, single, cont in contention_rows:
        hs = single.get("median_wall_s")
        hc = cont.get("hermes", {}).get("median_wall_s")
        dc = cont.get("honcho", {}).get("median_wall_s")
        delta = cont.get("hermes_delta_s")
        ne = cont.get("hermes", {}).get("content_nonempty_all")
        out.append(f"| {cell} | {fmt(hs)} | {fmt(hc)} | {fmt(delta)} | {fmt(dc)} | {fmt(ne)} |")

out.append("\n## Per-run details\n")
out.append("Raw JSON: `results/<cell>/single_run{1,2,3}.json`, `contention_{hermes,honcho}{1,2,3}.json`.\n")
out.append("Wall-clock timings: `*.time` files next to the responses.\n")
out.append("1 Hz resource samples: `results/<cell>/resources.tsv` (ts, VRAM used MiB, VRAM free MiB, RAM used MiB, RAM avail MiB).\n")
out.append("llama-server stdout+stderr per cell: `results/<cell>/server.log`.\n")

open(report_path, "w").write("\n".join(out) + "\n")
print(f"[py] wrote {report_path}", file=sys.stderr)
PY

info "all cells done. See $REPORT"
